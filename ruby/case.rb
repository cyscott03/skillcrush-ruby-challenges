weather = 'snowy'
case weather
when 'sunny'
  puts "Awesome! Sunglasses!"
when 'cloudy'
  puts "Might need a jacket or coat."
when 'rainy'
  puts "Better get that umbrella and raincoat!"
when 'snowy'
  puts "Brrr! Make sure you got a scarf, hat and gloves."
when 'stormy'
  puts "Stay inside"
else
  puts "Where whatever!"
end
